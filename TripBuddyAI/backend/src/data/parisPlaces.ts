// Helper function to calculate distance
export function calculateDistance(lat1: number, lng1: number, lat2: number, lng2: number): number {
  const R = 6371e3;
  const φ1 = (lat1 * Math.PI) / 180;
  const φ2 = (lat2 * Math.PI) / 180;
  const Δφ = ((lat2 - lat1) * Math.PI) / 180;
  const Δλ = ((lng2 - lng1) * Math.PI) / 180;

  const a = Math.sin(Δφ / 2) * Math.sin(Δφ / 2) +
            Math.cos(φ1) * Math.cos(φ2) *
            Math.sin(Δλ / 2) * Math.sin(Δλ / 2);
  const c = 2 * Math.atan2(Math.sqrt(a), Math.sqrt(1 - a));

  return R * c;
}

export function formatDistance(meters: number): string {
  if (meters < 1000) {
    return `${Math.round(meters)}m`;
  }
  return `${(meters / 1000).toFixed(1)}km`;
}

export function formatDuration(meters: number): string {
  const minutes = Math.round(meters / (5000 / 60));
  return `${Math.max(1, minutes)} мін`;
}

// Get places with calculated distances from user location
export function getParisPlacesWithDistance(userLat: number, userLng: number, type?: string): PlaceOption[] {
  let filtered = PARIS_PLACES;
  
  if (type && type !== 'all') {
    if (type === 'restaurant') {
      filtered = PARIS_PLACES.filter(p => ['restaurant', 'cafe', 'bakery'].includes(p.type));
    } else {
      filtered = PARIS_PLACES.filter(p => p.type === type);
    }
  }
  
  return filtered.map(place => {
    const dist = calculateDistance(userLat, userLng, place.location!.lat, place.location!.lng);
    return {
      ...place,
      distance: formatDistance(dist),
      duration: formatDuration(dist),
    };
  }).sort((a, b) => {
    // Sort by distance
    const distA = parseFloat(a.distance.replace(/[^\d.]/g, ''));
    const distB = parseFloat(b.distance.replace(/[^\d.]/g, ''));
    return distA - distB;
  });
}
